// Refactored Solution
for (var officer in officers){
  for (var i in votes) {
    if (isNaN(voteCount[officer][votes[i][officer]])){
      voteCount[officer][votes[i][officer]] = 1;
    }
    else {
    voteCount[officer][votes[i][officer]] += 1;
    };
  }
}

for (var officer in officers){
  var max_count = 0;
  for (var a in voteCount[officer]){
    if (voteCount[officer][a] > max_count) {
      officers[officer] = a;
      max_count = voteCount[officer][a];
    };
  };
};

